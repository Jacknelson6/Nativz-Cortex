export const SCRIPTING_FRAMEWORK = `## Video Scripting Principles

### Negativity Wins
Negativity catches eyes because it provokes stronger feelings than positive content. Indirectly challenge mistakes or misconceptions (e.g., "Squats just suck for growing your legs, try these instead"). Focus on the mistake, not the person. Use negativity to spark interest, not offend.

### The You-Format
Personalization warrants attention. The closer to a "1-on-1" feel, the more engaging.
- Replace plurals: "Everyone is looking for ways to save money" → "You're looking for ways to save money"
- Give ownership: "the bank account" → "your bank account"

### Script Rules
- As short as possible — only as long as needed to solve the viewer's problem
- Remove unnecessary context, get straight to the point
- Write at a 7th-grade reading level — don't dumb down info, just present it clearly

## Idea Generation Techniques to Apply

### Trend Jacking
Jump on trending topics. Speed > polish. Connect trends to the brand's niche creatively.

### Social Hacking
Reference big names, brands, or institutions to borrow their audience's attention. Say something NEW about them through the brand's lens.

### Pre-Validated Content
If something already blew up on Reddit, Twitter, or another platform, it's proven audiences care. Use upvotes/engagement as validation signals.

### Reverse Engineering
When referencing viral formats: identify the hook, body structure, storytelling devices (curiosity, humor, stakes, surprise), and editing cadence that made it work.

### Visual Pacifiers ("Pope in the Pool")
Suggest pairing educational voiceovers with stimulating visuals (cooking, gym, driving, nature) to keep viewers watching through complex info.

### Templatize Virality
When suggesting ideas similar to content that already worked: keep the structure, swap the variables (topic, stat, visual), match the energy.`;
